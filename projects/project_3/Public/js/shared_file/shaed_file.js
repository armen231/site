load();
function load() {
    if (sessionStorage.getItem("url") != null) {
        if (sessionStorage.getItem("url") != window.location.href) {
            sessionStorage.setItem("scroll", 0);
            document.querySelector(".body").scrollTop = 0;
        } else { document.querySelector(".body").scrollTop = sessionStorage.getItem("scroll"); }
    }
    sessionStorage.setItem("url", window.location.href);


    //////////////////////////////////////////////////////


    if (localStorage.getItem("theme") == null) {
        if (window.matchMedia) {
            if (window.matchMedia("(prefers-color-scheme: dark)").matches) { theme = "light"; click_theme(); }
            if (window.matchMedia("(prefers-color-scheme: light)").matches) { theme = "dark"; click_theme(); }
        }
        localStorage.setItem("theme", theme);
    } else {
        if (theme != localStorage.getItem("theme")) {
            setClassTheme();
            document.querySelector("body").classList.toggle("body-dark");
            theme = localStorage.getItem("theme");
        }
    }


    //////////////////////////////////////////////////////


    if (navigator.userAgent.match(/Android/i) || navigator.userAgent.match(/BlackBerry/i) ||
        navigator.userAgent.match(/iPhone|iPad|iPod/i) || navigator.userAgent.match(/Opera Mini/i) ||
        navigator.userAgent.match(/IEMobile/i) || navigator.userAgent.match(/RIM/i)
    ) { isTouch = 1; } else { isTouch = 0; }

    if (isTouch == 1) {
        document.querySelector("body .header .container-top .header .menu").classList.add("menu--touch1");
        document.querySelector("body .header .container-top .header .nav .bell .bell__order-bell").classList.add("bell__order-bell--touch1");
    }
    else {
        document.querySelector("body .header .container-top .header .menu").classList.add("menu--touch0");
        document.querySelector("body .header .container-top .header .nav .bell .bell__order-bell").classList.add("bell__order-bell--touch0");
    }
}


