document.querySelector("body .header .container-top .header .nav .function_list .theme").addEventListener("click", click_theme);
function click_theme() {

    if (theme == "light") {
        theme = "dark";
        localStorage.setItem("theme", theme);
        document.querySelector("body").classList.add("body-dark");
        // document.querySelector("body").classList.remove("body");
    }
    else if (theme == "dark") {
        theme = "light";
        localStorage.setItem("theme", theme);
        document.querySelector("body").classList.remove("body-dark");
        // document.querySelector("body").classList.add("body");
    }
    setClassTheme();
}

function setClassTheme() {
    document.querySelector("body .header .container-top .header .nav .function_list .theme .light").classList.toggle("active");
    document.querySelector("body .header .container-top .header .nav .function_list .theme .dark").classList.toggle("active");
}


